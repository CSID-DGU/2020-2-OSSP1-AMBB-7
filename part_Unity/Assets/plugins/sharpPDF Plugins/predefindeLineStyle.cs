namespace sharpPDF.Enumerators
{
	/// <summary>
	/// Enumerator that implements the style of a line.
	/// </summary>
	public enum predefinedLineStyle 
	{
		/// <summary>
		/// Normal line
		/// </summary>
		csNormal,
		/// <summary>
		/// Thin Outlined line
		/// </summary>
		csOutlinedSmall,
		/// <summary>
		/// Outlined line
		/// </summary>
		csOutlined,
		/// <summary>
		/// Big Outlined line
		/// </summary>
		csOutlinedBig
	}
}